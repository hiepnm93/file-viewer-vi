export const FILE_VIEWER_BUILTIN_MESSAGES = Object.freeze({
  'vi-VN': VI_VN_MESSAGES,
  'zh-CN': ZH_CN_MESSAGES,
  'en-US': EN_US_MESSAGES,
} satisfies Record<FileViewerResolvedLocale, Record<FileViewerMessageKey, string>>);

const getBrowserLocale = (): string | undefined => {
  if (typeof navigator === 'undefined') {
    return undefined;
  }
  return navigator.languages?.[0] || navigator.language;
};

export const normalizeFileViewerLocale = (
  locale?: FileViewerLocale | null
): FileViewerResolvedLocale => {
  const candidate = !locale || locale === 'auto'
    ? getBrowserLocale()
    : locale;
  const normalized = String(candidate || FILE_VIEWER_DEFAULT_LOCALE).toLowerCase();
  if (normalized.startsWith('vi')) {
    return 'vi-VN';
  }
  if (normalized.startsWith('zh')) {
    return 'zh-CN';
  }
  return 'en-US';
};

const getCustomMessages = (input?: FileViewerI18nInput): FileViewerMessages | undefined => {
  return input?.i18n?.messages || input?.messages;
};

export const resolveFileViewerLocale = (input?: FileViewerI18nInput) => {
  return normalizeFileViewerLocale(input?.i18n?.locale || input?.locale);
};

export const formatFileViewerMessage = (
  message: string,
  params: FileViewerMessageParams = {}
) => message.replace(/\{([a-zA-Z0-9_.-]+)\}/g, (_match, key: string) => {
  const value = params[key];
  return value === undefined || value === null ? '' : String(value);
});

export const translateFileViewerMessage = (
  input: FileViewerI18nInput,
  key: FileViewerMessageKey,
  params: FileViewerMessageParams = {}
) => {
  const locale = resolveFileViewerLocale(input);
  const customMessages = getCustomMessages(input);
  const customMessage = typeof customMessages === 'function'
    ? customMessages(key, params, locale)
    : customMessages?.[key];
  const message = customMessage || FILE_VIEWER_BUILTIN_MESSAGES[locale][key] || FILE_VIEWER_BUILTIN_MESSAGES[FILE_VIEWER_DEFAULT_LOCALE][key];
  return formatFileViewerMessage(message, params);
};

export const createFileViewerTranslator = (input?: FileViewerI18nInput) => {
  return (key: FileViewerMessageKey, params?: FileViewerMessageParams) =>
    translateFileViewerMessage(input, key, params);
};
